#include "../../INCLUDES/libft.h"

int	ft_ishexdigit(int c)
{
	if (ft_isdigit(c) == 1 || (c >= 'A' && c <= 'F') || (c >= 'a' && c <= 'f'))
		return (1);
	else
		return (0);
}
